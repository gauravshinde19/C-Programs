/* Program to print datatypes */

#include<stdio.h>

int main()
{
    int iValue = 10;
    float fValue = 20.89f;
    double dValue = 30.0689862;
    char cValue = 'G';

    printf("iValue : %d\n", iValue);
    printf("fValue : %.2f\n", fValue);
    printf("dValue : %f\n", dValue);
    printf("cValue : %c\n", cValue);
}